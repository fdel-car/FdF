#include "fdf.h"
#include <fcntl.h>
#include <unistd.h>

int		ft_color(t_draw *draw)
{
	if (draw->z1 == 0 && draw->z2 == 0)
		return (0xFFFFFF);
	else
		return (16776960);
}
